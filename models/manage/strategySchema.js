const mongoose = require("mongoose");

const strategy = new mongoose.Schema(
  {
    url: {
      type: String,
      required: true,
    },
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);
module.exports =
  mongoose.models.Strategy || mongoose.model("Strategy", strategy);
